    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        
        if (txtDocumentType.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el tipo de documento");
            txtDocumentType.requestFocus();
            return;
        }

        if (txtIdentificationNumber.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el numero de documento");
            txtIdentificationNumber.requestFocus();
            return;
        }

        if (txtName.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el nombre");
            txtName.requestFocus();
            return;
        }

        if (txtLastName.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite el apellido");
            txtLastName.requestFocus();
            return;
        }

        if (txtSex.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite su sexo");
            txtSex.requestFocus();
            return;
        }

        if (txtAddress.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite su direccion");
            txtAddress.requestFocus();
            return;
        }

        if (txtPhone.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite su numero de celular");
            txtPhone.requestFocus();
            return;
        }

        if (txtDate.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite la fecha de su nacimiento");
            txtDate.requestFocus();
            return;
        }

        if (txtCivilStatus.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite su estado civil");
            txtCivilStatus.requestFocus();
            return;
        }

        try {

            Funcionario funcionario = new Funcionario();
            funcionario.setTipo_identificacion(txtDocumentType.getText().trim());
            funcionario.setNumero_identificacion(txtIdentificationNumber.getText().trim()); // (String.valueOf(selectedFuncionario.getId())
            funcionario.setNombres(txtName.getText().trim());
            funcionario.setApellidos(txtLastName.getText().trim());
            funcionario.setSexo(txtSex.getText().trim());
            funcionario.setDireccion(txtAddress.getText().trim());
            funcionario.setTelefono(txtPhone.getText().trim());
            funcionario.setFecha_nacimiento(txtDate.getText().trim());
            funcionario.setEstado_civil(txtCivilStatus.getText().trim());
            funcionarioController.crear(funcionario);
            
            txtDocumentType.setText("");
            txtIdentificationNumber.setText("");
            txtName.setText("");
            txtLastName.setText("");
            txtSex.setText("");
            txtAddress.setText("");
            txtPhone.setText("");
            txtDate.setText("");
            txtCivilStatus.setText("");
            listFuncionarios();
            JOptionPane.showMessageDialog(null, "Funcionario creado con exito");

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "No fue creado el funcionario");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

