    private void listFuncionarios() {
        cbxFuncionarios.removeAllItems();
        Funcionario funcionario1 = new Funcionario();
        funcionario1.setNombres(SELECCIONE); 
        funcionario1.setApellidos(" ");// funcionario1.setTipo_identificacion(SELECCIONE);
        cbxFuncionarios.addItem(funcionario1);      
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for(String COLUMN : COLUMNS){
            defaultTableModel.addColumn(COLUMN);
        }
        
        tblFuncionarios.setModel(defaultTableModel);
        
        try {
            
            List<Funcionario> funcionarios = funcionarioController.obtenerFuncionarios();
            if (funcionarios.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(funcionarios.size());
            int row = 0;
            for (Funcionario funcionario : funcionarios) {
                defaultTableModel.setValueAt(funcionario.getId(), row, 0);
                defaultTableModel.setValueAt(funcionario.getTipo_identificacion(), row, 1);
                defaultTableModel.setValueAt(funcionario.getNumero_identificacion(), row, 2);
                defaultTableModel.setValueAt(funcionario.getNombres(), row, 3);
                defaultTableModel.setValueAt(funcionario.getApellidos(), row, 4);
                defaultTableModel.setValueAt(funcionario.getSexo(), row, 5);
                defaultTableModel.setValueAt(funcionario.getDireccion(), row, 6);
                defaultTableModel.setValueAt(funcionario.getTelefono(), row, 7);
                defaultTableModel.setValueAt(funcionario.getFecha_nacimiento(), row, 8);
                defaultTableModel.setValueAt(funcionario.getEstado_civil(), row, 9);
                row++;
                
                cbxFuncionarios.addItem(funcionario);
                
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
